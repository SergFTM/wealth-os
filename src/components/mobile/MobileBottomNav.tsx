interface MobileBottomNavProps {
  items?: NavItem[];
  className?: string;
}

export function MobileBottomNav({ items = defaultNavItems, className }: MobileBottomNavProps) {
  const pathname = usePathname();
  const router = useRouter();
  const { locale } = useApp();

  const isActive = (href: string) => {
    if (href === '/portal') {
      return pathname === '/portal' || pathname === '/portal/';
    }
    return pathname.startsWith(href);
  };

  return (
    <nav
      className={cn(
        'fixed bottom-0 left-0 right-0 z-50',
        'bg-white/90 backdrop-blur-lg border-t border-stone-200/50',
        'safe-area-bottom',
        className
      )}
    >
      <div className="flex items-center justify-around max-w-lg mx-auto px-2 py-2">
        {items.map((item) => {
          const active = isActive(item.href);
          return (
            <button
              key={item.id}
              onClick={() => router.push(item.href)}
              className={cn(
                'flex flex-col items-center justify-center min-w-[64px] py-1 px-3 rounded-xl transition-colors',
                active ? 'text-emerald-600' : 'text-stone-500 hover:text-stone-700'
              )}
            >
              <div className="relative">
                {active ? item.activeIcon : item.icon}
                {item.badge !== undefined && item.badge > 0 && (
                  <span className="absolute -top-1 -right-1 w-4 h-4 bg-red-500 text-white text-[10px] font-medium rounded-full flex items-center justify-center">
                    {item.badge > 9 ? '9+' : item.badge}
                  </span>
                )}
              </div>
              <span className={cn('text-[10px] mt-1', active ? 'font-medium' : '')}>
                {item.label[locale]}
              </span>
            </button>
          );
        })}
      </div>
    </nav>
  );
}
